/**
 * >   -  Maior que
 * >=  - Maior ou igual a
 * <   - Menor que
 * <=  - Menor ou igual a
 * ==  - igual a(valores)
 * === - igual a(valores e tipo)
 * !=  - diferente de(valores)
 * !== - diferente de(valores e tipo)
 */

const comp = 10 > 5;
console.log(`10 é maior do que 5?: `, comp);
const comp2 = 20 >= 20;
console.log(`20 é maior ou igual a 20:?`, comp2);
const comp3 = 25 <= 26;
console.log(`25 é menor ou igual a 26?:`, comp3)
const comp4 = 30 === 20;
console.log(`30 é igual a 25?: `,comp4)
const comp5 = 100 !== 200;
console.log(`100 é dferente de 200?: `, comp5)